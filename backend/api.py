# POST /upload
# -------------------------------------------------------
@app.post("/upload", dependencies=[Depends(verify_api_key)])
async def upload_file(file: UploadFile = File(...)):
    """
    Upload and index a document (.pdf or .txt, max 20 MB).
 
    Steps:
      1. Validate extension and file size
      2. Write to disk asynchronously (non-blocking)
      3. Extract text → chunk → embed → add to FAISS
      4. Persist chunks + FAISS index
    """
    ext = os.path.splitext(file.filename)[1].lower()
    if ext not in ALLOWED_EXTS:
        raise HTTPException(
            status_code=400,
            detail=f"Unsupported file type '{ext}'. Only .pdf and .txt are allowed."
        )
 
    # Read content once and check size before writing to disk
    content = await file.read()
    if len(content) > MAX_FILE_SIZE:
        raise HTTPException(
            status_code=413,
            detail=f"File too large ({len(content) // 1024} KB). Max allowed: 20 MB."
        )
 
    try:
        file_path = os.path.join(settings.UPLOAD_FOLDER, file.filename)
 
        # Async write — does NOT block the event loop
        async with aiofiles.open(file_path, "wb") as f:
            await f.write(content)
        logger.info(f"[Upload] Saved: {file_path}")
 
        text = load_document(file_path)
        if not text.strip():
            raise HTTPException(status_code=400, detail="File is empty or unreadable.")
 
        chunks = chunk_text(text, source=file.filename)
        if not chunks:
            raise HTTPException(status_code=400, detail="No usable content found in file.")
        logger.info(f"[Upload] {len(chunks)} chunks created.")
 
        embeddings = create_embeddings(chunks)
 
        # Use the singleton store — no repeated disk loads
        store = get_vector_store()
        store.add(embeddings)
        store.save()
 
        total_chunks = save_chunks(chunks)
 
        return {
            "message":       f"'{file.filename}' uploaded and indexed.",
            "chunks_added":  len(chunks),
            "total_chunks":  total_chunks,
            "total_vectors": store.total_vectors,
        }
 
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"[Upload] Failed: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail=f"Upload failed: {str(e)}")
 
 
