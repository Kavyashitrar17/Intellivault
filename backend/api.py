# DELETE /reset
# -------------------------------------------------------
@app.delete("/reset", dependencies=[Depends(verify_api_key)])
def reset():
    """
    Clear all indexed data (FAISS index + chunks file).
    Resets the in-memory singleton too.
    Does NOT delete uploaded source files.
    """
    from backend.retrieval.vector_store import reset_vector_store
    deleted = []
 
    for path in [settings.INDEX_PATH, settings.CHUNKS_PATH]:
        if os.path.exists(path):
            os.remove(path)
            deleted.append(path)
            logger.info(f"[Reset] Deleted {path}")
 
    reset_vector_store()  # clear singleton so next request starts fresh
 
    return {"message": "Index and chunks cleared.", "deleted": deleted}
