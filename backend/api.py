# GET /status
# -------------------------------------------------------
@app.get("/status")
def status():
    """
    Returns system health — vector count and chunk count.
    Does NOT load all chunks into RAM (fast).
    """
    store        = get_vector_store()
    total_chunks = count_chunks()  # just reads metadata, not full data
 
    return {
        "status":        "ok",
        "total_vectors": store.total_vectors,
        "total_chunks":  total_chunks,
        "index_synced":  store.total_vectors == total_chunks,
    }
 
 
